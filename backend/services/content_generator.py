SALES_TOPICS = {"кейс", "оффер", "призыв", "услуги"}


def generate_post(topic: str = None) -> dict:
    if not topic or topic not in TOPICS:
        topic = random.choice(list(TOPICS.keys()))

    if topic in SALES_TOPICS:
        body = random.choice(BODIES.get(topic, BODIES["AUREON"]))
        headline = random.choice(HEADLINES.get(topic, [body.split("\n")[0]]))
        tag = TAGS.get(topic, "AIдлябизнеса")
        content = body
    else:
        headline = random.choice(HEADLINES.get(topic, HEADLINES["AI"]))
        body = random.choice(BODIES.get(topic, BODIES["AI"]))
        cta = random.choice(CTAS)
        template = random.choice(POST_TEMPLATES)
        tag = TAGS.get(topic, "AI")
        content = template.format(headline=headline, body=body, cta=cta, tag=tag)

    title_words = headline.split()[:6]
    title = " ".join(title_words)

    return {
        "title": title,
        "content": content,
        "topic": topic,
        "status": "draft",
        "platform": "telegram",
        "tags": [topic, "AUREON", "AI"],
    }
